def hooke_plane_stress(E, nu):
    """3×3 plane-stress constitutive matrix D."""
    f = E / (1.0 - nu**2)
    return f * np.array([[1., nu, 0.],
                          [nu, 1., 0.],
                          [0., 0., (1.-nu)/2.]])


def Bu_and_detJ(xi1v, xi2v, xe_nodes):
    """
    In-plane B matrix (3×8) and Jacobian determinant for the 4-node
    bilinear element.
    xe_nodes : (4,2) nodal coords.
    """
    dN_dxi = 0.25 * np.array([
        [-(1.-xi2v),  (1.-xi2v), (1.+xi2v), -(1.+xi2v)],
        [-(1.-xi1v), -(1.+xi1v),(1.+xi1v),  (1.-xi1v)]
    ])                              # (2,4)
    J    = dN_dxi @ xe_nodes        # (2,2)
    detJ = float(np.linalg.det(J))
    dN_dx = np.linalg.inv(J) @ dN_dxi  # (2,4)  [row0=dNi/dx, row1=dNi/dy]

    B_u = np.zeros((3, 8))
    for k in range(4):
        B_u[0, 2*k  ] = dN_dx[0, k]    # ∂N/∂x → εxx
        B_u[1, 2*k+1] = dN_dx[1, k]    # ∂N/∂y → εyy
        B_u[2, 2*k  ] = dN_dx[1, k]    # ∂N/∂y → γxy
        B_u[2, 2*k+1] = dN_dx[0, k]    # ∂N/∂x → γxy
    return B_u, detJ


def Nu_inplane(xi1v, xi2v):
    """Shape function matrix (2×8) for in-plane bilinear element."""
    Nv = [0.25*(1.-xi1v)*(1.-xi2v),
          0.25*(1.+xi1v)*(1.-xi2v),
          0.25*(1.+xi1v)*(1.+xi2v),
          0.25*(1.-xi1v)*(1.+xi2v)]
    N_u = np.zeros((2, 8))
    for k, Nk in enumerate(Nv):
        N_u[0, 2*k  ] = Nk
        N_u[1, 2*k+1] = Nk
    return N_u


