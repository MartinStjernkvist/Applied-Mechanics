# =============================================================================
# 4. Element routine (Task 1c / 1d)
#
# Returns the 4 element matrices:
#   K_uu  (8×8)   in-plane membrane stiffness
#   K_ww  (12×12) Kirchhoff bending stiffness
#   f_u   (8×1)   in-plane load vector  (snow tangential component)
#   f_w   (12×1)  bending load vector   (snow transverse component)
# =============================================================================
def kirchhoff_plate_element(ex, ey, h_pl, E, nu, q_transverse, fx_inplane):
    """
    Combined Kirchhoff plate element matrices.

    Parameters
    ----------
    ex, ey       : (4,) nodal x and y coordinates [m]
    h_pl         : plate thickness [m]
    E, nu        : material parameters
    q_transverse : uniform transverse pressure [N/m²]  (→ f_w)
    fx_inplane   : uniform in-plane load in x-dir [N/m²] (→ f_u)

    Returns
    -------
    K_uu (8,8), K_ww (12,12), f_u (8,1), f_w (12,1)
    """
    D      = hooke_plane_stress(E, nu)
    D_bar  = (h_pl**3 / 12.0) * D       # flexural stiffness matrix

    xe_nodes = np.column_stack((ex, ey))  # (4,2)
    n1 = np.array([[ex[0]], [ey[0]]])
    n2 = np.array([[ex[1]], [ey[1]]])
    n3 = np.array([[ex[2]], [ey[2]]])
    n4 = np.array([[ex[3]], [ey[3]]])

    K_uu = np.zeros((8,  8))
    K_ww = np.zeros((12, 12))
    f_u  = np.zeros((8,  1))
    f_w  = np.zeros((12, 1))

    for i in range(4):                   # 2×2 Gauss integration
        xi1v, xi2v = _xi_gp[i]
        xin = np.array([[xi1v], [xi2v]])

        # ── in-plane (membrane) ──────────────────────────────────────────
        B_u, detJ = Bu_and_detJ(xi1v, xi2v, xe_nodes)
        N_u       = Nu_inplane(xi1v, xi2v)
        f_body    = np.array([[fx_inplane], [0.0]])

        K_uu += B_u.T @ (h_pl * D) @ B_u * detJ       # weight = 1
        f_u  += N_u.T @ f_body * detJ                  # N/m² × m² → N

        # ── bending (Kirchhoff) ──────────────────────────────────────────
        _, Bast, detFis = _Bast_kirchoff_func(xin, n1, n2, n3, n4)
        detFis_val = float(np.squeeze(detFis))

        N_w = _N_kirchoff_func(xin, n1, n2, n3, n4)
        N_w_col = N_w.reshape(-1, 1)               # ensure (12,1)

        K_ww += Bast.T @ D_bar @ Bast * detFis_val
        f_w  += N_w_col * q_transverse * detFis_val

    return K_uu, K_ww, f_u, f_w


