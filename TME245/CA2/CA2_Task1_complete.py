# 10. Stress computation at integration points  (Task 1f)
#
# At each of the 4 Gauss points (ξ1,ξ2) ∈ {±1/√3}² and at thickness
# coordinate z, the total in-plane strain is
#
#     ε(z) = ε⁰  −  z · κ
#
# where  ε⁰ = B_u · aᵤ   (membrane)
#         κ  = Bast · aω  (curvature; from Kirchhoff element)
#
# and the Cauchy stress vector (Voigt, plane stress) is
#
#     σ = D · ε(z)  =  [σ₁₁, σ₂₂, σ₁₂]ᵀ
#
# Von Mises effective stress (plane stress, σ₃₃=σ₁₃=σ₂₃=0):
#     σ_vM = √(σ₁₁² − σ₁₁σ₂₂ + σ₂₂² + 3σ₁₂²)
# =============================================================================

def von_mises_ps(sigma):
    """Von Mises for plane-stress σ = [s11, s22, s12]."""
    s11, s22, s12 = sigma
    return np.sqrt(s11**2 - s11*s22 + s22**2 + 3.0*s12**2)


def element_stress_at_z(ex, ey, a_u_el, a_w_el, h_pl, E, nu, z_coord):
    """
    Compute von Mises stress at all 4 Gauss points for one element
    at through-thickness position z.

    Parameters
    ----------
    a_u_el : (8,)  element in-plane DOF values
    a_w_el : (12,) element bending DOF values
    z_coord: z position  (e.g. +h/2, 0, -h/2)

    Returns
    -------
    sigma_vM : (4,)  von Mises stress at each Gauss point [Pa]
    """
    D         = hooke_plane_stress(E, nu)
    xe_nodes  = np.column_stack((ex, ey))
    n1=np.array([[ex[0]],[ey[0]]])
    n2=np.array([[ex[1]],[ey[1]]])
    n3=np.array([[ex[2]],[ey[2]]])
    n4=np.array([[ex[3]],[ey[3]]])

    sigma_vM = np.zeros(4)
    for i in range(4):
        xi1v, xi2v = _xi_gp[i]
        xin = np.array([[xi1v],[xi2v]])

        # Membrane strain
        B_u, _ = Bu_and_detJ(xi1v, xi2v, xe_nodes)
        eps0   = B_u @ a_u_el                          # (3,)

        # Curvature  κ = Bast · aω  (Bast has shape (3,12))
        _, Bast, _ = _Bast_kirchoff_func(xin, n1, n2, n3, n4)
        kappa  = Bast @ a_w_el                         # (3,)

        # Total strain at depth z  (Kirchhoff: ε = ε⁰ - z·κ)
        eps_z  = eps0 - z_coord * kappa

        # Stress and von Mises
        sigma = D @ eps_z
        sigma_vM[i] = von_mises_ps(sigma)

    return sigma_vM


