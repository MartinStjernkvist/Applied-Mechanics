def _N_kirchoff_func(in1, in2, in3, in4, in5):
    """Kirchhoff plate shape functions N  (returns shape (12,1))."""
    xe11=in2[0,:]; xe12=in2[1,:]
    xe21=in3[0,:]; xe22=in3[1,:]
    xe31=in4[0,:]; xe32=in4[1,:]
    xe41=in5[0,:]; xe42=in5[1,:]
    xi1=in1[0,:];  xi2=in1[1,:]
    t2=xi1+1.0; t3=xi2+1.0; t4=xi1**2; t5=xi2**2
    t6=xi1-1.0; t7=xi2-1.0
    t8=t4*xe11; t9=t5*xe11; t10=t4*xe12; t11=t5*xe12
    t12=t4*xe21;t13=t5*xe21;t14=t4*xe22;t15=t5*xe22
    t16=t4*xe31;t17=t5*xe31;t18=t4*xe32;t19=t5*xe32
    t20=t4*xe41;t21=t5*xe41;t22=t4*xe42;t23=t5*xe42
    mt1=np.array([t6*t7*(t4+t5+t6+t7)*(-1./8.),
                  (t6*t7*(t8+t9-t12-t21-xe11*2.+xe21+xe41))/16.,
                  (t6*t7*(t10+t11-t14-t23-xe12*2.+xe22+xe42))/16.,
                  (t2*t7*(-t2+t4+t5+t7))/8.,
                  t2*t7*(-t8+t12+t13-t17+xe11-xe21*2.+xe31)*(-1./16.),
                  t2*t7*(-t10+t14+t15-t19+xe12-xe22*2.+xe32)*(-1./16.),
                  (t2*t3*(t2+t3-t4-t5))/8.,
                  (t2*t3*(-t13+t16+t17-t20+xe21-xe31*2.+xe41))/16.,
                  (t2*t3*(-t15+t18+t19-t22+xe22-xe32*2.+xe42))/16.])
    mt2=np.array([(t3*t6*(-t3+t4+t5+t6))/8.,
                  t3*t6*(-t9-t16+t20+t21+xe11+xe31-xe41*2.)*(-1./16.),
                  t3*t6*(-t11-t18+t22+t23+xe12+xe32-xe42*2.)*(-1./16.)])
    N=np.concatenate([mt1, mt2], axis=0)   # (12, 1) when xi are (1,) arrays
    return N                                # shape (12,1) or (12,) depending on input


def _detFisop_4node(in1, in2, in3, in4, in5):
    """Jacobian determinant for 4-node isoparametric element."""
    in1=np.atleast_2d(in1).reshape(2,-1)
    in2=np.atleast_2d(in2).reshape(2,-1)
    in3=np.atleast_2d(in3).reshape(2,-1)
    in4=np.atleast_2d(in4).reshape(2,-1)
    in5=np.atleast_2d(in5).reshape(2,-1)
    xe11=in2[0,:]; xe12=in2[1,:]
    xe21=in3[0,:]; xe22=in3[1,:]
    xe31=in4[0,:]; xe32=in4[1,:]
    xe41=in5[0,:]; xe42=in5[1,:]
    xi1=in1[0,:];  xi2=in1[1,:]
    dN1_dxi1=-0.25*(1.-xi2); dN2_dxi1= 0.25*(1.-xi2)
    dN3_dxi1= 0.25*(1.+xi2); dN4_dxi1=-0.25*(1.+xi2)
    dN1_dxi2=-0.25*(1.-xi1); dN2_dxi2=-0.25*(1.+xi1)
    dN3_dxi2= 0.25*(1.+xi1); dN4_dxi2= 0.25*(1.-xi1)
    dx_dxi1=dN1_dxi1*xe11+dN2_dxi1*xe21+dN3_dxi1*xe31+dN4_dxi1*xe41
    dy_dxi1=dN1_dxi1*xe12+dN2_dxi1*xe22+dN3_dxi1*xe32+dN4_dxi1*xe42
    dx_dxi2=dN1_dxi2*xe11+dN2_dxi2*xe21+dN3_dxi2*xe31+dN4_dxi2*xe41
    dy_dxi2=dN1_dxi2*xe12+dN2_dxi2*xe22+dN3_dxi2*xe32+dN4_dxi2*xe42
    return dx_dxi1*dy_dxi2 - dx_dxi2*dy_dxi1


